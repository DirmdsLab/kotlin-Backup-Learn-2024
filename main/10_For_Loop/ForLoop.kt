fun main() {
    // For Loop 
    val Tsundere = arrayOf("Hmph","Moo Shiranai","Au Ah")

    for(i in Tsundere) {
        println(i)
    }
    println()

    // Part 2
    for(i in 0..10){
        print("$i ")
    }
    println()

    for(i in 100 downTo 1 step 20) {
        print("$i ")
    }
    println()

}